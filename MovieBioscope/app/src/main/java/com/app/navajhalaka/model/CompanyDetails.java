package com.app.navajhalaka.model;



public class CompanyDetails {
    private String name;
    private String url;

    public String getName() {
        return name;
    }

    public CompanyDetails setName(String name) {
        this.name = name;
        return this;
    }

    public String getUrl() {
        return url;
    }

    public CompanyDetails setUrl(String url) {
        this.url = url;
        return this;
    }
}
